const HREFS = Object.keys(ICONS);

function replaceIcons() {
	for (const href of HREFS) {
		const link = document.querySelector<HTMLElement>(`a[href="${href}"]`);
		if (!link || link.hasAttribute(ICON_ATTR)) continue;

		const svg = link.querySelector("svg");
		if (!svg) continue;

		const tmp = document.createElement("div");
		tmp.innerHTML = ICONS[href];
		const newSvg = tmp.firstElementChild as SVGElement;

		newSvg.style.color = "inherit";
		svg.replaceWith(newSvg);
		link.setAttribute(ICON_ATTR, "1");
	}

	// Replace the budget name dropdown chevron
	const budgetBtn = document.querySelector<HTMLElement>('button[style*="sidebarBudgetName"]');
	if (budgetBtn && !budgetBtn.hasAttribute(ICON_ATTR)) {
		const svg = budgetBtn.querySelector("svg");
		if (svg) {
			const tmp = document.createElement("div");
			tmp.innerHTML = icon("chevronDown", { size: 14 });
			const newSvg = tmp.firstElementChild as SVGElement;
			newSvg.style.color = "inherit";
			newSvg.style.flexShrink = "0";
			newSvg.style.marginLeft = "5px";
			newSvg.style.opacity = "0.5";
			svg.replaceWith(newSvg);
			budgetBtn.setAttribute(ICON_ATTR, "1");
		}
	}
}

export const sidebarIcons = defineSetting({
	type: "checkbox",
	label: "Modern Sidebar Icons",
	context: {
		key: "modern-sidebar-icons",
		defaultValue: true,
	},
	init: () => {
		const unwatch = watchDom(replaceIcons);

		return () => {
			unwatch();
			document.querySelectorAll(`[${ICON_ATTR}]`).forEach((el) => {
				el.removeAttribute(ICON_ATTR);
			});
			window.location.reload();
		};
	},
});
